# Исправлено ИИ

import random

while True:
    # Шаг 2: Попросить игрока выбрать: 1-камень, 2-ножницы, 3-бумага
    print("Выбери: 1-камень, 2-ножницы, 3-бумага")
    
    # Шаг 3: Прочитать и проверить выбор игрока
    player_choice = int(input("Твой выбор (1-3): "))  # [ДОПИШИ САМ]: обработку ошибок ввода
    
    # Шаг 4: Сгенерировать случайный выбор компьютера
    computer_choice = random.randint(1, 3)
    
    # Шаг 5-8: Сравнить выборы и определить победителя
    if player_choice == computer_choice:
        print("Ничья")
    elif (player_choice == 1 and computer_choice == 2) or \
         (player_choice == 2 and computer_choice == 3) or \
         (player_choice == 3 and computer_choice == 1):
        print("Ты победил!")
    else:
        print("Компьютер победил!")
    
    # Шаг 9: Спросить, хочет ли игрок сыграть ещё
    play_again = input("Сыграем ещё? (да/нет): ").lower()  # [ДОПИШИ САМ]: проверку корректности ответа
    if play_again != "да":
        print("Конец игры")
        break  # Шаг 12: Конец, выход из цикла




